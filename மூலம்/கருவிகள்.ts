import {எழுத்துகள்_அடைப்புகள்} from './எழுத்துகள்/குறியீட்டு.js';

export function எழுத்து_குறிமுறை_சரிபார்த்தல்({
  எழுத்து, குறிமுறை
}: {
  எழுத்து: string,
  குறிமுறை: string
}): void {
  if (!எழுத்துகள்_அடைப்புகள்[எழுத்து]) {
    throw `தெரியாத எழுத்து "${எழுத்து}"`
  }
  if (!எழுத்துகள்_அடைப்புகள்[எழுத்து][குறிமுறை]) {
    throw `"${எழுத்து}" எழுத்துக்கு தெரியாத குறிமுறை "${குறிமுறை}"`
  }
}
